class LoginActivity : AppCompatActivity() {
lateinit var binding:ActivityLoginBinding
    lateinit var auth: FirebaseAuth
    lateinit var email:String
    lateinit var password:String
    lateinit var email_field:TextInputEditText
    lateinit var password_fieled:TextInputEditText
    val emailPattern:Regex =Regex("[a-zA-Z0-9._-]+@[a-z]+\\.+[a-z]+")
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
       binding = DataBindingUtil.setContentView(this,R.layout.activity_login)
        intializer()

        findViewById<AppCompatButton>(R.id.login_btn).setOnClickListener()
        {
            binding.loginProgressbar.visibility= View.VISIBLE
            loginUser()
        }
        binding.loginSignup.setOnClickListener()
        {
            startActivity(Intent(this,RegistrationActivity::class.java))
        }


    }

    private fun intializer() {
        auth = FirebaseAuth.getInstance()
        email_field=findViewById(R.id.login_email)
        password_fieled=findViewById(R.id.login_password)

    }

    private  fun loginUser()
    {
        email = email_field.text.toString().trim()
        password = password_fieled.text.toString()
        if(TextUtils.isEmpty(email)&&TextUtils.isEmpty(password)) {
          binding.loginProgressbar.visibility=View.INVISIBLE
            if(TextUtils.isEmpty(email)&&TextUtils.isEmpty(password))
                Toast.makeText(this@LoginActivity, "enter email and password", Toast.LENGTH_SHORT).show()

            else if (email.isEmpty())
                Toast.makeText(this@LoginActivity, "enter email", Toast.LENGTH_SHORT).show()
            else
                Toast.makeText(this@LoginActivity, "enter password", Toast.LENGTH_SHORT).show()
        }else if (!email.matches(emailPattern))    {      binding.loginProgressbar.visibility=View.INVISIBLE

        email_field.error = "invalid email"}
        else if(password.length<6) {
            binding.loginProgressbar.visibility = View.INVISIBLE

                password_fieled.error="invalid password"}
        else{
            auth.signInWithEmailAndPassword(email,password).addOnCompleteListener(
                OnCompleteListener {task->
                    if(task.isSuccessful)
                    {
                        binding.loginProgressbar.visibility= View.INVISIBLE


                        startActivity(Intent(this@LoginActivity,HomeActivity::class.java))
                    }
                    else
                    {
                        binding.loginProgressbar.visibility= View.INVISIBLE

                        Toast.makeText(this@LoginActivity,"error in login",Toast.LENGTH_LONG).show()
                    }
                })
        }
    }


